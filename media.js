// arrays
// vamos calcular uma media de valores
// declarando o vetor
const notas = [10, 6.5, 8, 7.5];
// vamos imprimir para ver se esta tudo certo
console.log(`As notas sao ${notas} !`);
// fazer o calculo
const media = (notas[0] + notas[1] + notas[2] + notas[3]) / notas.length;
// imprimir a media
console.log(`A media eh ${media} !`);